# --- Загрузка с вращением ---
def loading_animation(duration=4000):
    symbols = ["|", "/", "-", "\\"]
    start_time = pygame.time.get_ticks()
    angle = 0
    while pygame.time.get_ticks() - start_time < duration:
        for event in pygame.event.get():
            if event.type == pygame.QUIT or (event.type == pygame.KEYDOWN and event.key == pygame.K_ESCAPE):
                pygame.quit()
                sys.exit()

        screen.fill(BLACK)
        angle += 0.05

        # --- Логотип ---
        for p in points:
            r = rotateY(p, angle)
            x, y = project(r)
            screen.blit(logo_font.render("#", True, GREEN), (x, y))

        # --- FSOCIETY снизу ---
        base_y = HEIGHT - len(fs_text)*24 - 50
        for i, line in enumerate(fs_text):
            txt = text_font.render(line, True, GREEN)
            screen.blit(txt, (WIDTH//2 - txt.get_width()//2, base_y + i*24))

        # --- Прогресс ---
        elapsed = pygame.time.get_ticks() - start_time
        total = duration
        bar_len = 40
        filled = int(bar_len * elapsed / total)
        bar = "#"*filled + "-"*(bar_len-filled)
        progress_txt = text_font.render(bar, True, GREEN)
        screen.blit(progress_txt, (WIDTH//2 - progress_txt.get_width()//2, HEIGHT - 50))
        spinner = symbols[(elapsed//100) % len(symbols)]
        spin_txt = text_font.render(spinner, True, GREEN)
        screen.blit(spin_txt, (WIDTH//2 + progress_txt.get_width()//2 + 10, HEIGHT - 50))

        pygame.display.flip()
        clock.tick(60)

#Терминал
def terminal():
    lines = []
    input_text = ""
    while True:
        for event in pygame.event.get():
            if event.type == pygame.QUIT or (event.type == pygame.KEYDOWN and event.key == pygame.K_ESCAPE):
                pygame.quit()
                sys.exit()
            elif event.type == pygame.KEYDOWN:
                if event.key == pygame.K_RETURN:
                    lines.append("> " + input_text)
                    input_text = ""
                elif event.key == pygame.K_BACKSPACE:
                    input_text = input_text[:-1]
                else:
                    input_text += event.unicode

        screen.fill(BLACK)

        # --- Строки терминала ---
        y = 10
        for line in lines[-(HEIGHT//24-1):]:
            txt = term_font.render(line, True, GREEN)
            screen.blit(txt, (10, y))
            y += 24

        txt = term_font.render("> " + input_text, True, GREEN)
        screen.blit(txt, (10, y))

        pygame.display.flip()
        clock.tick(60)

# --- Запуск ---
loading_animation(duration=4000)
terminal()
# --- Масштабирование для 3D-точек ---
scale = 10
points = []
logo_height = len(ascii_logo)
logo_width = max(len(line) for line in ascii_logo)
for y, line in enumerate(ascii_logo):
    for x, c in enumerate(line):
        if c != " ":
            points.append([(x - logo_width / 2) * scale, (y - logo_height / 2) * scale, 0])

# --- Вращение и проекция ---
def rotateY(p, angle):
    x, y, z = p
    c, s = math.cos(angle), math.sin(angle)
    return [x*c - z*s, y, x*s + z*c]

def project(p):
    x, y, z = p
    dist = 1000
    f = 800 / (z + dist)
    return int(x*f + WIDTH/2), int(y*f + HEIGHT/2 - 100)

